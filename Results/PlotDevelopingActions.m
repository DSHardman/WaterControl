function radar(state, fs, row)
    subplot(5,3,(row-1)*3+1);
    load('Training/RF2Agent1000.mat');
    spider_plot(cell2mat(saved_agent.getAction(state)).',...
        'FillOption', 'on', 'AxesDisplay', 'one',...
        'AxesLimits', [-ones(1,11); ones(1,11)],...
        'AxesLabelsEdge', 'none', 'Marker', '.', 'MarkerSize', 20,...
        'Color', 1/255*[217 95 25], 'LabelFontSize', fs,...
        'AxesLabels', 'none');
    subplot(5,3,(row-1)*3+2);
    load('Training/RF2Agent3000.mat');
    spider_plot(cell2mat(saved_agent.getAction(state)).',...
        'FillOption', 'on', 'AxesDisplay', 'one',...
        'AxesLimits', [-ones(1,11); ones(1,11)],...
        'AxesLabelsEdge', 'none', 'Marker', '.', 'MarkerSize', 20,...
        'Color', 1/255*[217 95 25], 'LabelFontSize', fs,...
        'AxesLabels', 'none');
    subplot(5,3,(row-1)*3+3);
    load('Training/RF2Agent5000.mat');
    spider_plot(cell2mat(saved_agent.getAction(state)).',...
        'FillOption', 'on', 'AxesDisplay', 'one',...
        'AxesLimits', [-ones(1,11); ones(1,11)],...
        'AxesLabelsEdge', 'none', 'Marker', '.', 'MarkerSize', 20,...
        'Color', 1/255*[217 95 25], 'LabelFontSize', fs,...
        'AxesLabels', 'none');
end